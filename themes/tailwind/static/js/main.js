function openNav() {
	// document.getElementById("mobile-menu-burger")
	document.getElementById("mobile-menu").classList.add("h-[100%]");
	document.getElementById("mobile-menu").classList.remove("hidden", "h-0");
}

function closeNav() {
	document.getElementById("mobile-menu").classList.remove("h-[100%]");
	document.getElementById("mobile-menu").classList.add("h-0", "hidden");
}
